# 4) Define ROI for each plane
#    Edit these values to match your Rx region
# =========================================================

# Example ROI for XZ plane
ROI_XZ = {
    "x_min": -1,
    "x_max": 2,
    "z_min": -20,
    "z_max": -10
}

# Example ROI for YZ plane
ROI_YZ = {
    "y_min": -10,
    "y_max": 10,
    "z_min": -3,
    "z_max": 2
}

# =========================================================
# 5) Compute ROI metrics
# =========================================================
summary_rows = []
node_export_rows = []

for fname, item in parsed.items():
    df = item["df"].copy()
    plane = item["plane_detected"]
    mode = item["mode"]

    # Select ROI
    if plane == "XZ":
        roi = df[
            (df["x"] >= ROI_XZ["x_min"]) & (df["x"] <= ROI_XZ["x_max"]) &
            (df["z"] >= ROI_XZ["z_min"]) & (df["z"] <= ROI_XZ["z_max"])
        ].copy()

    elif plane == "YZ":
        roi = df[
            (df["y"] >= ROI_YZ["y_min"]) & (df["y"] <= ROI_YZ["y_max"]) &
            (df["z"] >= ROI_YZ["z_min"]) & (df["z"] <= ROI_YZ["z_max"])
        ].copy()

    else:
        roi = pd.DataFrame(columns=df.columns)

    # Metrics
    if len(roi) == 0:
        E_peak = np.nan
        E_mean = np.nan
        E_std = np.nan
        N_points = 0
    else:
        E_peak = roi["Field"].max()
        E_mean = roi["Field"].mean()
        E_std  = roi["Field"].std(ddof=1) if len(roi) > 1 else 0.0
        N_points = len(roi)

    summary_rows.append({
        "Mode": mode,
        "Plane": plane,
        "Filename": fname,
        "ROI_Points": N_points,
        "E_peak (V/m)": E_peak,
        "E_mean (V/m)": E_mean,
        "E_standardDeviation (V/m)": E_std,
        "File_Min (V/m)": item["minval_file"],
        "File_Max (V/m)": item["maxval_file"]
    })

    # Optional: keep ROI node data for checking
    if len(roi) > 0:
        roi_export = roi.copy()
        roi_export.insert(0, "Mode", mode)
        roi_export.insert(1, "Plane", plane)
        roi_export.insert(2, "Filename", fname)
        node_export_rows.append(roi_export)

summary_df = pd.DataFrame(summary_rows)
summary_df = summary_df.sort_values(["Plane", "Mode"]).reset_index(drop=True)

if len(node_export_rows) > 0:
    roi_nodes_df = pd.concat(node_export_rows, ignore_index=True)
else:
    roi_nodes_df = pd.DataFrame(columns=["Mode", "Plane", "Filename", "x", "y", "z", "Field"])

print(summary_df)

