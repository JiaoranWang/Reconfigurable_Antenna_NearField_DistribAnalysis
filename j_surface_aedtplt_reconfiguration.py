def extract_block(text, key):
    start = text.find(f"{key}(")
    if start == -1:
        raise ValueError(f"{key}( not found in file")

    i = start + len(key) + 1
    depth = 1
    out = []

    while i < len(text) and depth > 0:
        ch = text[i]
        if ch == "(":
            depth += 1
        elif ch == ")":
            depth -= 1
            if depth == 0:
                break
        out.append(ch)
        i += 1

    return "".join(out)

def parse_float_list(s):
    return [float(x) for x in re.findall(r'[-+]?\d*\.?\d+(?:[eE][-+]?\d+)?', s)]

def parse_int_list(s):
    return [int(x) for x in re.findall(r'[-+]?\d+', s)]

def parse_nodes(nodes_text):
    vals = parse_float_list(nodes_text)
    return np.array(vals).reshape(-1, 3)

def parse_elements(elements_text):
    arr = parse_int_list(elements_text)
    total_nodes = arr[0]
    total_elements = arr[1]

    pos = 2
    elements = []

    for _ in range(total_elements):
        header = arr[pos:pos+5]
        n_nodes = header[4]
        conn = arr[pos+5:pos+5+n_nodes]
        pos += 5 + n_nodes
        elements.append({
            "header": header,
            "n_nodes": n_nodes,
            "conn": conn
        })

    return total_nodes, total_elements, elements

def parse_elem_solution(elemsol_text):
    vals = parse_float_list(elemsol_text)
    minval = vals[0]
    maxval = vals[1]
    n_per_element = int(vals[2])
    data = vals[3:]
    return minval, maxval, n_per_element, data

def build_nodal_scalar_field(nodes, elements, n_per_element, elem_data):
    nodal_sum = np.zeros(len(nodes), dtype=float)
    nodal_count = np.zeros(len(nodes), dtype=int)

    pos = 0
    for el in elements:
        n_nodes = el["n_nodes"]
        vals = elem_data[pos:pos+n_per_element]
        pos += n_per_element

        if n_per_element != n_nodes:
            raise ValueError(f"Scalar file expected n_per_element={n_nodes}, got {n_per_element}")

        for node_idx_1based, fval in zip(el["conn"], vals):
            idx = node_idx_1based - 1
            nodal_sum[idx] += fval
            nodal_count[idx] += 1

    return nodal_sum / np.where(nodal_count == 0, 1, nodal_count)

def build_nodal_vector_field(nodes, elements, n_per_element, elem_data):
    vec_sum = np.zeros((len(nodes), 3), dtype=float)
    vec_count = np.zeros(len(nodes), dtype=int)

    pos = 0
    for el in elements:
        n_nodes = el["n_nodes"]
        vals = elem_data[pos:pos+n_per_element]
        pos += n_per_element

        if n_per_element != 3 * n_nodes:
            raise ValueError(f"Vector file expected n_per_element={3*n_nodes}, got {n_per_element}")

        for local_idx, node_idx_1based in enumerate(el["conn"]):
            idx = node_idx_1based - 1
            jx = vals[3 * local_idx + 0]
            jy = vals[3 * local_idx + 1]
            jz = vals[3 * local_idx + 2]
            vec_sum[idx, 0] += jx
            vec_sum[idx, 1] += jy
            vec_sum[idx, 2] += jz
            vec_count[idx] += 1

    return vec_sum / np.where(vec_count[:, None] == 0, 1, vec_count[:, None])

def parse_mode_and_kind_from_filename(filepath):
    base = os.path.basename(filepath)
    low = base.lower()

    mode_match = re.search(r'\[?([01]{3})\]?', base)
    mode = mode_match.group(1) if mode_match else "UNK"

    if ("mag_jsurf" in low) or ("mag" in low and "jsurf" in low):
        kind = "MAG"
    elif ("vec_jsurf" in low) or ("vector_jsurf" in low) or ("vec" in low and "jsurf" in low):
        kind = "VEC"
    else:
        kind = "UNK"

    return mode, kind

def parse_aedtplt_file(filepath):
    with open(filepath, "r", encoding="utf-8", errors="ignore") as f:
        text = f.read()

    nodes = parse_nodes(extract_block(text, "Nodes"))
    _, _, elements = parse_elements(extract_block(text, "Elements"))
    minval, maxval, n_per_element, elem_data = parse_elem_solution(extract_block(text, "ElemSolution"))

    mode, kind_name = parse_mode_and_kind_from_filename(filepath)
    n_nodes_first = elements[0]["n_nodes"]

    if n_per_element == n_nodes_first:
        jmag = build_nodal_scalar_field(nodes, elements, n_per_element, elem_data)
        df = pd.DataFrame({
            "x": nodes[:, 0],
            "y": nodes[:, 1],
            "z": nodes[:, 2],
            "Jmag": jmag
        })
        data_type = "MAG"

    elif n_per_element == 3 * n_nodes_first:
        jvec = build_nodal_vector_field(nodes, elements, n_per_element, elem_data)
        jmag = np.linalg.norm(jvec, axis=1)
        df = pd.DataFrame({
            "x": nodes[:, 0],
            "y": nodes[:, 1],
            "z": nodes[:, 2],
            "Jx": jvec[:, 0],
            "Jy": jvec[:, 1],
            "Jz": jvec[:, 2],
            "Jmag": jmag
        })
        data_type = "VEC"

    else:
        raise ValueError(f"Unsupported file format: n_per_element={n_per_element}")

    return {
        "filepath": filepath,
        "filename": os.path.basename(filepath),
        "mode": mode,
        "kind": data_type if kind_name == "UNK" else kind_name,
        "df": df
    }

