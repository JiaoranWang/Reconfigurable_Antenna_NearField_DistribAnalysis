def select_region(df, reg):
    return df[
        (df["x"] >= reg["x_min"]) & (df["x"] <= reg["x_max"]) &
        (df["y"] >= reg["y_min"]) & (df["y"] <= reg["y_max"]) &
        (df["z"] >= reg["z_min"]) & (df["z"] <= reg["z_max"])
    ].copy()

# =========================================================
# 6) Compute J_Pi and V_Pi
# =========================================================
rows = []
all_modes = sorted(set(mag_files.keys()) | set(vec_files.keys()))

for mode in all_modes:
    mag_df = mag_files.get(mode, {}).get("df", pd.DataFrame())
    vec_df = vec_files.get(mode, {}).get("df", pd.DataFrame())

    for pname, reg in PIN_REGIONS.items():
        # --------------------------
        # Current concentration: J_Pi
        # --------------------------
        mag_roi = select_region(mag_df, reg) if not mag_df.empty else pd.DataFrame()

        if len(mag_roi) == 0:
            JPi = np.nan
            N_mag = 0
        else:
            JPi = mag_roi["Jmag"].mean()
            N_mag = len(mag_roi)

        # --------------------------
        # Flow direction: V_Pi
        # --------------------------
        vec_roi = select_region(vec_df, reg) if not vec_df.empty else pd.DataFrame()

        if len(vec_roi) == 0:
            VPi = np.nan
            N_vec = 0
        else:
            v0 = np.array(reg["v0"], dtype=float)
            v0 = v0 / np.linalg.norm(v0)

            J = vec_roi[["Jx", "Jy", "Jz"]].to_numpy()
            Jmag = np.linalg.norm(J, axis=1)

            proj = np.sum(J * v0[None, :], axis=1) / (Jmag + 1e-12)
            VPi = np.mean(proj)
            N_vec = len(vec_roi)

        rows.append({
            "Mode": f"[{mode}]" if mode != "UNK" else mode,
            "PIN_Region": pname,
            "N_mag": N_mag,
            "J_Pi (A/m)": JPi,
            "N_vec": N_vec,
            "V_Pi": VPi
        })

result_df = pd.DataFrame(rows)

